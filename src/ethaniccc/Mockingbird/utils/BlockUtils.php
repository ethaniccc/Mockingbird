namespace ethaniccc\Mockingbird\utils;


use pocketmine\block\Air;
use pocketmine\block\Block;
use pocketmine\block\Fallable;
use pocketmine\block\Transparent;

class BlockUtils {
	public static function canClip(Block $block) : bool {
		if($block instanceof Fallable || $block instanceof Transparent || $block instanceof Air) {
			return true;
		}
		return false;
	}
}
